          <h2>Disaster Risk Reduction Management Division (DRRMD) </h2>
          <p>
          The DICT’s Disaster Risk Reduction Management Division (DRRMD) 
          under the Office of the Undersecretary for Resilient Government Emergency Communications also  operates 24/7 to provide emergency telecommunications services to government agencies when needed.

          </p>
          <p>
          The DICT-DRRMD as lead agency of the Emergency Telecommunications Cluster through the National Disaster Risk Reduction and Management Plan (NDRRMP) fulfills the requirement of RA
          No. 10121 of 2010, which provides the legal basis for policies, plans and programs to deal with
          disasters. The NDRRMP covers four thematic areas, namely, (1) Disaster Prevention and Mitigation;
          (2) Disaster Preparedness; (3) Disaster Response; and (4) Disaster Rehabilitation and Recovery,
          which correspond to the structure of the National Disaster Risk Reduction and Management Council
          (NDRRMC). By law, the Office of Civil Defense formulates and implements the NDRRMP and ensures
          that the physical framework, social, economic and environmental plans of communities, cities,
          municipalities and provinces are consistent with such plan.
          </p>
          <p>
          The NDRRMP is consistent with the National Disaster Risk Reduction and Management Framework
          (NDRRMF), which serves as “the principal guide to disaster risk reduction and management (DRRM)
          efforts to the country….” The Framework envisions a country of “safer, adaptive and disasterresilient Filipino communities toward sustainable development.” It conveys a paradigm shift from
          reactive to proactive DRRM wherein men and women have increased their awareness and
          understanding of DRRM, with the end in view of increasing people’s resilience and decreasing their
          vulnerabilities.

          </p>
